CREATE TABLE WOW_MEMBERINFO
(ID VARCHAR2(20) PRIMARY KEY, PW VARCHAR2(20), NAME VARCHAR2(20), PHONE VARCHAR2(20), EMAIL VARCHAR2(20), MAILADDR VARCHAR2(20), BIRTHDATE DATE);

CREATE TABLE WOW_EXTRAINFO
(MEMBERNUM NUMBER(10) PRIMARY KEY, ID VARCHAR2(20), GENDER VARCHAR2(5), TEL VARCHAR2(20), NICKNAME VARCHAR2(20), MEMBERTYPE VARCHAR2(10), ADDRESS1 VARCHAR2(50), ADDRESS2 VARCHAR2(50), COMMENTS VARCHAR2(1000), LICENSENUM VARCHAR2(20));

